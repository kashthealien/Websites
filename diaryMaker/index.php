<h2 class="blue-back">Diary Maker</h2>
<p>
 Diary maker is a free desktop software that allows you to analyse your chats.<br/>
It uses your chats database obtained from Offline GMail and reads the database to analyse your chats.
Some of the features range from plotting the trends of your chats with people against your calendar,
 ranking of contacts based on your chats and so on.<br/>
In addition a chat search engine, chat tagger (which associates your chats with key words), sentiment
analysis of your chats, automated detection of possible events from your chats and facebook posts and
chat translation are being developed for release in the next version.
